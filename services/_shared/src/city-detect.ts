/**
 * Общая функция определения города/региона по тексту объявления.
 *
 * Использует regex с word-boundary, чтобы избежать ложных срабатываний
 * (например, 'Москва-Кашира' не будет распознано как 'москва').
 */

/** Ключевые слова Московской области */
const MO_KEYWORDS = [
  'московская область',
  'московская обл',
  'московская',
  'подольск',
  'химки',
  'красногорск',
  'лобня',
  'мытищи',
  'балашиха',
  'одинцово',
  'пушкино',
  'серпухов',
  'котельники',
  'луховицк',
  'домодедов',
  'люберц',
];

/**
 * Регионы, которые НЕ являются Москвой/МО.
 * Если текст содержит любой из этих маркеров, возвращаем 'other' сразу
 * (если только нет явных признаков МО). Это предотвращает ложное срабатывание,
 * когда шаблонный текст содержит «Москва», а адрес — другой регион.
 */
const NON_MOSCOW_REGIONS = [
  // Республики
  'республика дагестан', 'республика башкортостан', 'республика татарстан',
  'республика крым', 'чеченская республика', 'республика марий эл',
  'республика мордовия', 'республика коми', 'республика карелия',
  'республика хакасия', 'республика алтай', 'республика бурятия',
  'республика саха', 'республика калмыкия', 'республика ингушетия',
  'республика северная осетия', 'республика адыгея',
  'республика кабардино-балкария', 'республика карачаево-черкесия',
  // Края
  'алтайский край', 'краснодарский край', 'красноярский край',
  'приморский край', 'хабаровский край', 'ставропольский край', 'пермский край',
  // Крупные города (не-Москва/МО)
  'новосибирск', 'екатеринбург', 'нижний новгород', 'казань',
  'краснодар', 'ростов-на-дону', 'самара', 'воронеж', 'пермь',
  'омск', 'волгоград', 'челябинск', 'красноярск', 'тюмень',
  'саратов', 'тольятти', 'ижевск', 'барнаул', 'иркутск',
  'ульяновск', 'хабаровск', 'владивосток', 'ярославль',
  'махачкала', 'томск', 'оренбург', 'кемерово', 'рязань',
  'астрахань', 'пенза', 'липецк', 'киров', 'тула',
  'калининград', 'брянск', 'курск', 'тверь', 'магнитогорск',
  'иваново', 'владимир',
  // Области
  'свердловская область', 'нижегородская область', 'новосибирская область',
  'самарская область', 'челябинская область', 'ростовская область',
  'воронежская область', 'краснодарский край', 'пермский край',
  'оленегорск', 'сочи', 'анапа', 'геленджик',
];

/** Regex для точного определения «Москва» как отдельного слова (НЕ Москва-Кашира) */
const MOSCOW_RE = /(^|[\s,.()])москва([\s,.()]|$)/i;

/**
 * Определяет город/регион по тексту объявления.
 *
 * @param text — произвольный текст (заголовок, адрес, описание)
 * @returns 'moscow' | 'mo' | 'other'
 */
export function detectCity(text: string): 'moscow' | 'mo' | 'other' {
  const lower = text.toLowerCase();

  // Московская область — по ключевым словам (проверяем первым, т.к. имеет приоритет)
  if (MO_KEYWORDS.some((kw) => lower.includes(kw))) {
    return 'mo';
  }

  // Если текст содержит маркер не-московского региона — сразу 'other',
  // даже если где-то в шаблонном тексте есть «Москва»
  if (NON_MOSCOW_REGIONS.some((kw) => lower.includes(kw))) {
    return 'other';
  }

  // Проверяем «Москва» как отдельное слово (не «Москва-Кашира» и т.п.)
  if (MOSCOW_RE.test(lower)) {
    if (!lower.includes('московская')) {
      return 'moscow';
    }
  }

  return 'other';
}
